package day4_arithmetic_operators;

public class additionConcat {
	
	public static void main(String[] args) {
		
		System.out.println(2+2);
		System.out.println(20+200);
		System.out.println(5+"hello");
		System.out.println("hello"+true);
		System.out.println("hello"+"world");
		
		// System.out.println(true+1);
		
		System.out.println(2+0+(3+"Cybertek"));
		System.out.println("Cybertek"+2+0+5);
		System.out.println(2+4+5+"Cybertek"+2+0+5);
		System.out.println(2+(3+5+"Cybertek"+(2+4)+1));
		
		
		
		
	}

}
