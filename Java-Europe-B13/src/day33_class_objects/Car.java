package day33_class_objects;

public class Car {
	
	String model;
	String make;
	String colour;
	int year;
	double mile;
	
	public Car() {
		
	}
	public Car(String model, String make, String colour, int year, double mile) {
		this.model=model;
		this.make=make;
		this.colour= colour;
		this.year=year;
		this.mile=mile;
		
		
	}

}
